        <h2>O que são cookies?</h2>
        <p>Como é prática comum em quase todos os sites profissionais, este site usa cookies, que são pequenos ficheiros descarregados para o seu computador, para melhorar a sua experiência. Esta página descreve as informações que eles recolhem, como as usamos e por que às vezes precisamos de armazenar esses cookies.</p>
        
        <h2>Como usamos os cookies?</h2>
        <p>Utilizamos cookies por vários motivos, detalhados abaixo. Infelizmente, na maioria dos casos, não existem opções padrão do setor para desativar os cookies sem desativar completamente a funcionalidade e os recursos que eles adicionam a este site.</p>
        
        <h2>Cookies que definimos</h2>
        <ul>
            <li><strong>Cookies relacionados a formulários:</strong> Quando submete dados através de um formulário, como os encontrados nas páginas de contacto ou de checkout, os cookies podem ser configurados para lembrar os detalhes do utilizador para correspondência futura.</li>
            <li><strong>Cookies de preferências do site:</strong> Para lhe proporcionar uma ótima experiência neste site, fornecemos a funcionalidade para definir as suas preferências de como este site é executado quando o utiliza.</li>
        </ul>
        
        <h2>Cookies de Terceiros</h2>
        <p>Em alguns casos especiais, também usamos cookies fornecidos por terceiros de confiança. Este site usa plataformas de análise e publicidade (como Taboola, Reddit e Google Analytics), que são algumas das soluções analíticas mais difundidas e confiáveis da web, para nos ajudar a entender como usa o site e como podemos melhorar a sua experiência.</p>
        <p>Esses cookies podem rastrear itens como quanto tempo gasta no site e as páginas visitadas, para que possamos continuar a produzir conteúdo atraente e otimizar os nossos anúncios.</p>
        
        <h2>Desativar Cookies</h2>
